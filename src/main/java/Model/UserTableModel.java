package Model;

import java.sql.Date;

public class UserTableModel {
    int id;
    String cod, username, firstname, lastname, cedula, telefono,email,cargo,turno;
    Date hiredate;

    public UserTableModel(int id, String cod, String username,
                          String firstname, String lastname, String cedula, String telefono,
                          String email, String cargo, String turno, Date hiredate) {
        this.id = id;
        this.cod = cod;
        this.username = username;
        this.firstname = firstname;
        this.lastname = lastname;
        this.cedula = cedula;
        this.telefono = telefono;
        this.email = email;
        this.cargo = cargo;
        this.turno = turno;
        this.hiredate = hiredate;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCod() {
        return cod;
    }

    public void setCod(String cod) {
        this.cod = cod;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    public String getTurno() {
        return turno;
    }

    public void setTurno(String turno) {
        this.turno = turno;
    }

    public Date getHiredate() {
        return hiredate;
    }

    public void setHiredate(Date hiredate) {
        this.hiredate = hiredate;
    }


}
